// given the days of the weeks in short formate "sun","mon"
//....print in long formate "sunday" , "monday".

let day= "mo";

switch(day){
    case "sun": console.log("Sunday");
                break;
    case "mon": console.log("Monday");
                break;
    case "tus": console.log("Tuseday");
                break;
    case "wed": console.log("Wednesday");
                break;
    case "thu": console.log("Thrusday");
                break;
    case "fri": console.log("Friday");
                break;
    case "sat": console.log("Satday");
                break;
    default : console.log("Invalid Input");
}